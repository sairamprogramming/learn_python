# Program demonstrates bitwise operators in python.

a, b = 60, 2

print(f"a = {a}")
print(f"b = {b}")

# Bitwise Operators

print('a&b', a&b)   # Binary AND
print('a|b', a|b)   # Binary OR
print('a^b', a^b)   # Binary XOR
print('~a', ~a)     # Binary Ones Complement
print('a>>b', a>>b) # Binary Right Shift
print('a<<b', a<<b) # Binary left shift
