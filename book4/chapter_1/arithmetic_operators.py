# Program demonstrates arithemtic operators in python.

a, b, c = 10, 5, 2

print(f"a = {a}")
print(f"b = {b}")
print(f"c = {c}")

# Arithmetic Operators.
print(f"a + b = {a + b}")
print(f"a - b = {a - b}")
print(f"a * b = {a * b}")
print(f"a / b = {a / b}")
print(f"b % c = {b % c}")   # Remainder
print(f"b**2 = {b**2}")     # Exponentiation
print(f"b//c = {b//c}")     # Floor Division
